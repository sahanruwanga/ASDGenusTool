    private void predictASDButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_predictASDButtonActionPerformed
        // Set name, age, and gender on report
        nameResultText.setText(nameText.getText().trim());
        if(!ageText.getText().trim().isEmpty())
        ageResultText.setText(ageText.getText().trim() + " years");
        String gender = genderComboBox.getSelectedItem().toString();
        if (gender != "Select One"){
            genderResultText.setText(gender);
        }else{
            genderResultText.setText("");
        }

        if(!EEGFile.getInstance().getFilePath().isEmpty()){
            ArrayList<String> prediction = Predictor.getInstance().predictASD(EEGFile.getInstance().getFilePath());
            setReportDate(true);
            resultText.setText(prediction.get(0));
            descriptionText.setText(prediction.get(1));
            noOfChannelsDetailsText.setText(prediction.get(2));
            channelNamesDetailsText.setText(prediction.get(3));
            durationDetailsText.setText(prediction.get(4));
            recordedDateDetailsText.setText(prediction.get(5));
            saveResultButton.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(PredictPanel, "Choose a file first, before predicting");
        }
    }//GEN-LAST:event_predictASDButtonActionPerformed

    private void browseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseBtnActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Choose File");
        FileNameExtensionFilter vhdrFilter = new FileNameExtensionFilter("vhdr files (*.vhdr)", "vhdr");
        fileChooser.setFileFilter(vhdrFilter);
        int response = fileChooser.showOpenDialog(eegImportOuterPanel);
        if (response == JFileChooser.APPROVE_OPTION){
            browseTxt.setText(fileChooser.getSelectedFile().getName());
            EEGFile.getInstance().setFilePath(fileChooser.getSelectedFile().toString());
            EEGFile.getInstance().setFileName(fileChooser.getSelectedFile().getName());
            predictASDButton.setEnabled(true);
        }
    }//GEN-LAST:event_browseBtnActionPerformed

