    private void saveResultButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveResultButtonActionPerformed
        if(nameResultText.getText().trim() == "" || nameResultText.getText().trim() == null){
            JOptionPane.showMessageDialog(PredictPanel, "Name is essential for saving data!");
        }else{
            EEGSignal eegSignal = new EEGSignal(noOfChannelsDetailsText.getText().trim(), durationDetailsText.getText().trim(),
                channelNamesDetailsText.getText().trim(), recordedDateDetailsText.getText().trim());
            Report report = new Report(nameResultText.getText().trim(), ageResultText.getText().trim(),
                genderResultText.getText().trim(), dateResultText.getText().trim(),
                resultText.getText().trim(), descriptionText.getText().trim(), eegSignal);
            new ReportWriter().saveReport(report, eegSignal);
            JOptionPane.showMessageDialog(PredictPanel, "Result saved successfully!");
        }
    }//GEN-LAST:event_saveResultButtonActionPerformed

